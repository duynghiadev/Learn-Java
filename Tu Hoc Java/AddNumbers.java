public class AddNumbers {

    public static void main(String[] args) {
        // Define two numbers to be added
        int num1 = 5;
        int num2 = 10;

        // Calculate the sum of the numbers
        int sum = num1 + num2;

        // Print the sum to the console
        System.out.println("The sum of " + num1 + " and " + num2 + " is " + sum);
    }
}
